package com.finocio.block10_extras.exercises;

/**
 * Exercise03:
 * Declara una constante final llamada MAX_SCORE con valor 100 y muéstrala.
 */
public class Exercise03 {
    public static final int MAX_SCORE = 100;

    public static void main(String[] args) {
        System.out.println("La puntuación máxima es: " + MAX_SCORE);
    }
}
/*
Explicación:
- final hace que la variable no se pueda modificar.
- Las constantes suelen escribirse en mayúsculas.
*/
